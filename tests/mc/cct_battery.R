for (d in unique(cmp$dgp)) {
  sub <- cmp[cmp$dgp == d, ]
  if (nrow(sub) == 2L && all(c("cct", "symmetric") %in% sub$method)) {
    cct_rmse <- sub$rmse[sub$method == "cct"]
    sym_rmse <- sub$rmse[sub$method == "symmetric"]
    winner <- if (is.finite(cct_rmse) && is.finite(sym_rmse)) {
      if (cct_rmse < sym_rmse) "CCT" else "symmetric"
    } else "N/A"
    cat(sprintf("  %s: CCT RMSE=%.4f  |  symmetric RMSE=%.4f  ->  winner: %s\n",
                d, cct_rmse, sym_rmse, winner))
  }
}

