        alpha_grid_val = seq(0.88, 0.98, by = 0.02),
        alpha_s_val    = 0.98,
        true_beta = true_betas$DGP4,
        note = "60%+ censored; alpha_star = 0.85")

# ---- DGP 5: Weak Z (small K, small signal) ----
run_dgp("DGP5_weakZ", dgp5_weakZ, methods = c("cct", "symmetric"),
