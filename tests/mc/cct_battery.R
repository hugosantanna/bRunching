# Monte Carlo battery for the CCT-2025 estimator in bRunching.
#
# Purpose:
#   Stress-test ccn(method = "cct") across six DGPs varying the error
#   distribution, bunching rate, covariate strength, and sample size. For
#   two DGPs, compare CCT against the CCN "symmetric" estimator to see
#   where location-scale buys over symmetry.
#
# Inputs:   (none — simulates data)
# Outputs:
#   - tests/mc/cct_battery_results.csv
#   - printed summary table to stdout with pass/fail verdict per DGP
#
# Dependencies: devtools (for load_all), bRunching (source in this repo),
#               stats (rnorm/rbinom/quantile — base R). No sn dependency:
#               skew errors are generated by a two-component normal mixture.
#
# Target parameter:  beta(0+) = 1.5  (all DGPs)
# Pass criterion (per DGP, CCT): |bias| < 3 * empirical_SD / sqrt(B)
#                                (DGP 5 "weak Z" is allowed to fail).
# ---------------------------------------------------------------------------- #

