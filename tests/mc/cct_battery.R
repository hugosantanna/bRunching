# Pretty-print
fmt_row <- function(r) {
  verdict <- if (isTRUE(r$pass)) "PASS"
             else if (isTRUE(r$allow_fail)) "FAIL(allowed)"
             else                  "FAIL"
  sprintf("  %-18s  %-9s  B=%3d  truth=%.4f  bias=%+7.4f  SD=%6.4f  RMSE=%6.4f  [%s]",
          r$dgp, r$method, r$B_ok, r$beta_true, r$bias, r$sd, r$rmse, verdict)
}
for (i in seq_len(nrow(results))) {
  cat(fmt_row(results[i, , drop = FALSE]), "\n", sep = "")
}

