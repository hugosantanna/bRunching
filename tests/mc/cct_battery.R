# ---- Configuration --------------------------------------------------------- #
BETA_STRUCTURAL <- 1.5     # coefficient on X * (X > 0) in the DGP (structural)
B_MC      <- 100L          # replications per DGP
N_DEFAULT <- 5000L         # per-rep sample size (except DGP 6)
GLOBAL_RUNTIME_BUDGET_SEC <- 600  # if runtime so far exceeds this, skip DGP 6
N_CALIB   <- 200000L       # sample size for per-DGP CCT-estimand calibration
B_CALIB   <- 3L            # replications for per-DGP calibration
# NOTE: the CCT estimand beta(0+) (Prop 5.1) equals the structural 1.5 only
# when Assumption 7 and the symmetry implicit in the DGP both hold. For DGP3
# (skew) the population-scale Prop 5.1 expression gives a different value.
# We therefore calibrate the true CCT estimand per DGP and report bias
# relative to BOTH the structural 1.5 and the true CCT estimand.

# ---- Helpers --------------------------------------------------------------- #

# Skew-normal-like draw via two-component normal mixture with asymmetric
# weights. The mixture is normalized to have zero median and unit-ish scale,
# then shifted so the empirical median is zero (satisfying alpha_star = 0.5).
# This substitutes for sn::rsn (not installed in this environment).
rskew <- function(n, right_skew = TRUE) {
  p  <- 0.75                                # weight on the "narrow" component
  mu1 <- if (right_skew) -0.35 else  0.35   # narrow component mean
  mu2 <- if (right_skew)  1.05 else -1.05   # broad, heavier-tail component mean
  s1  <- 0.6
  s2  <- 1.3
  comp <- rbinom(n, 1, p)                   # 1 = narrow, 0 = broad
  z <- comp * rnorm(n, mu1, s1) + (1 - comp) * rnorm(n, mu2, s2)
  # Re-center to zero median so that alpha_star = 0.5 still satisfies
  # H^{-1}(alpha_star) = 0 in the boundary quantile condition.
  z - stats::median(z)
}

# Run one Monte Carlo rep for a DGP closure.
one_rep <- function(dgp_fn, method = "cct",
                    alpha_star_val = 0.5,
                    alpha_grid_val = NULL,
                    alpha_s_val    = NULL,
                    boot_B_val     = 0L) {
  sim <- dgp_fn()
  df  <- data.frame(y = sim$y, x = sim$x, z = sim$z)

  if (method == "cct") {
    fit <- tryCatch(
      ccn("y", "x", "z", df,
          method     = "cct",
          alpha_star = alpha_star_val,
          alpha_grid = alpha_grid_val,
          alpha_s    = alpha_s_val,
          boot_B     = boot_B_val,
          seed       = NULL),
      error = function(e) list(beta = NA_real_, error = conditionMessage(e))
    )
  } else if (method == "symmetric") {
    fit <- tryCatch(
      ccn("y", "x", "z", df, method = "symmetric"),
      error = function(e) list(beta = NA_real_, error = conditionMessage(e))
    )
  } else {
    stop("unknown method: ", method)
  }

  if (is.null(fit$beta) || length(fit$beta) == 0) return(NA_real_)
  unname(fit$beta[1])
}

# Summarize a vector of beta_hats into bias / SD / RMSE / pass-flag.
# Bias is computed relative to `true_beta`. If `structural_beta` is supplied
# and differs from `true_beta`, we also report bias relative to the structural
# parameter for diagnostic purposes.
summarise_reps <- function(beta_hats,
                           true_beta = BETA_STRUCTURAL,
                           structural_beta = BETA_STRUCTURAL,
                           allow_fail = FALSE) {
  ok  <- is.finite(beta_hats)
  Bok <- sum(ok)
  if (Bok < 3L) {
    return(data.frame(
      B_ok = Bok, beta_true = true_beta, bias = NA_real_,
      bias_vs_structural = NA_real_, sd = NA_real_, rmse = NA_real_,
      mc_se_bias = NA_real_, pass = FALSE, allow_fail = allow_fail
    ))
  }
  b    <- mean(beta_hats[ok]) - true_beta
  bs   <- mean(beta_hats[ok]) - structural_beta
  sdh  <- stats::sd(beta_hats[ok])
  rmse <- sqrt(mean((beta_hats[ok] - true_beta)^2))
  mc_se_bias <- sdh / sqrt(Bok)
  pass <- abs(b) < 3 * mc_se_bias
  data.frame(
    B_ok = Bok, beta_true = true_beta, bias = b,
    bias_vs_structural = bs, sd = sdh, rmse = rmse,
    mc_se_bias = mc_se_bias, pass = pass, allow_fail = allow_fail
  )
}

# Calibrate the true CCT estimand beta(0+) for a DGP using Prop 5.1 at
# a large sample size. `dgp_fn_with_mz` must return a list with $y, $x, $z,
# and $mz (the per-obs true m(Z_i)).
calibrate_true_beta <- function(dgp_fn_with_mz, n_big = N_CALIB, B = B_CALIB) {
  betas <- numeric(B)
  for (b in seq_len(B)) {
    s <- dgp_fn_with_mz(n_big)
    ok <- s$x > 0
    if (sum(ok) < 100L) { betas[b] <- NA_real_; next }
    fit <- stats::lm(s$y[ok] ~ s$x[ok])
    gamma_0 <- coef(fit)[1L]
    gamma_1 <- coef(fit)[2L]
    # Delta = E[Y | X = 0] - gamma_0 (= lim_{x->0+} E[Y | X = x])
    Delta <- mean(s$y[s$x == 0]) - gamma_0
    # pi = (E[m(Z)] - E[X]) / P(X = 0)
    pi <- (mean(s$mz) - mean(s$x)) / mean(s$x == 0)
    betas[b] <- unname(gamma_1 - Delta / pi)
  }
  mean(betas, na.rm = TRUE)
}

# ---- DGP definitions (closures returning $y, $x, $z) ----------------------- #

# Each DGP factory takes `n` and returns a list with $y, $x, $z, $mz (where
# mz is the per-obs value of m(Z_i), used for population-scale calibration).
dgp1_baseline <- function(n = N_DEFAULT, K = 20L) {
  Z <- sample.int(K, n, replace = TRUE)
  mu_z <- -0.5 + 0.15 * Z
  eta  <- rnorm(n)
  x_star <- mu_z + eta
  x <- pmax(x_star, 0)
  y <- 2 + 0.8 * x_star + BETA_STRUCTURAL * x * (x > 0) + rnorm(n)
  list(y = y, x = x, z = Z, mz = mu_z)
}

dgp2_heterosk <- function(n = N_DEFAULT, K = 20L) {
  Z <- sample.int(K, n, replace = TRUE)
  mu_z  <- -0.5 + 0.15 * Z
  sig_z <- 0.5 + Z / 20
  eta   <- rnorm(n, 0, sig_z)
  x_star <- mu_z + eta
  x <- pmax(x_star, 0)
  y <- 2 + 0.8 * x_star + BETA_STRUCTURAL * x * (x > 0) + rnorm(n)
  list(y = y, x = x, z = Z, mz = mu_z)
}

dgp3_skew <- function(n = N_DEFAULT, K = 20L) {
  Z <- sample.int(K, n, replace = TRUE)
  mu_z <- -0.5 + 0.15 * Z
  eta  <- rskew(n, right_skew = TRUE)     # zero-median asymmetric noise
  x_star <- mu_z + eta
  x <- pmax(x_star, 0)
  y <- 2 + 0.8 * x_star + BETA_STRUCTURAL * x * (x > 0) + rnorm(n)
  list(y = y, x = x, z = Z, mz = mu_z)
}

dgp4_highbunch <- function(n = N_DEFAULT, K = 10L) {
  # Heavy overall bunching (~50%) with alpha_star = 0.85 and noise
  # shifted so H^{-1}(0.85) = 0. m(z) kept strictly positive so the
  # short-circuit m_hat(z) = q_hat(z; alpha*) is correct in all cells.
  # Per-cell bunching ranges roughly 30-65%; overall ~50%.
  shift <- qnorm(0.85)
  Z <- sample.int(K, n, replace = TRUE)
  mu_z <- 0.5 + 0.1 * Z           # m(z) in (0.6, 1.5), all positive
  eta  <- rnorm(n) - shift
  x_star <- mu_z + eta
  x <- pmax(x_star, 0)
  y <- 2 + 0.8 * x_star + BETA_STRUCTURAL * x * (x > 0) + rnorm(n)
  list(y = y, x = x, z = Z, mz = mu_z)
}

dgp5_weakZ <- function(n = N_DEFAULT, K = 3L) {
  Z <- sample.int(K, n, replace = TRUE)
  mu_z <- -0.2 + 0.05 * Z
  eta  <- rnorm(n)
  x_star <- mu_z + eta
  x <- pmax(x_star, 0)
  y <- 2 + 0.8 * x_star + BETA_STRUCTURAL * x * (x > 0) + rnorm(n)
  list(y = y, x = x, z = Z, mz = mu_z)
}

# DGP 6 is a grid over n — run per-n via a factory.
dgp6_large_n <- function(n) function() dgp1_baseline(n = n, K = 20L)

# ---- Run the battery ------------------------------------------------------- #

all_rows <- list()
t0 <- Sys.time()

# ---- One-time calibration of true CCT estimand per DGP --------------------- #
# The CCT estimand beta(0+) (per Prop 5.1) does NOT always equal the structural
# coefficient 1.5 — e.g. under skew eta (DGP3), Assumption 7's quantitative
# implications change. We calibrate per DGP at large n using the Prop 5.1
# plug-in formula on population quantities.
cat("Calibrating true CCT estimand per DGP (n = ", N_CALIB, ", B = ", B_CALIB, ") ... ",
    sep = "")
true_betas <- list(
  DGP1 = calibrate_true_beta(dgp1_baseline),
  DGP2 = calibrate_true_beta(dgp2_heterosk),
  DGP3 = calibrate_true_beta(dgp3_skew),
  DGP4 = calibrate_true_beta(dgp4_highbunch),
  DGP5 = calibrate_true_beta(dgp5_weakZ)
)
cat("done.\n")
for (nm in names(true_betas)) {
  cat(sprintf("  %s: true beta(0+) = %.4f (structural = %.4f)\n",
              nm, true_betas[[nm]], BETA_STRUCTURAL))
}

run_dgp <- function(name, dgp_fn, methods = c("cct"),
                    alpha_star_val = 0.5,
                    alpha_grid_val = NULL,
                    alpha_s_val    = NULL,
                    true_beta = BETA_STRUCTURAL,
                    B = B_MC,
                    allow_fail = FALSE,
                    note = "") {
  cat(sprintf("\n=== %s (B = %d, methods: %s) ===\n",
              name, B, paste(methods, collapse = ", ")))
  cat(sprintf("    CCT target (Prop 5.1 estimand): %.4f   |   structural (symmetric target): %.4f\n",
              true_beta, BETA_STRUCTURAL))
  for (m in methods) {
    # CCT estimates beta(0+) (Prop 5.1). The CCN "symmetric" estimator
    # targets the pooled structural slope; evaluate it against that.
    truth_m <- if (m == "cct") true_beta else BETA_STRUCTURAL
    beta_hats <- rep(NA_real_, B)
    t_start <- Sys.time()
    for (b in seq_len(B)) {
      beta_hats[b] <- one_rep(dgp_fn, method = m,
                              alpha_star_val = alpha_star_val,
                              alpha_grid_val = alpha_grid_val,
                              alpha_s_val    = alpha_s_val,
                              boot_B_val     = 0L)
    }
    dt <- as.numeric(difftime(Sys.time(), t_start, units = "secs"))
    s  <- summarise_reps(beta_hats,
                         true_beta = truth_m,
                         structural_beta = BETA_STRUCTURAL,
                         allow_fail = allow_fail)
    row <- cbind(
      dgp = name, method = m, runtime_sec = round(dt, 2),
      s, note = note, stringsAsFactors = FALSE
    )
    cat(sprintf("  method=%-9s  truth=%.4f  B_ok=%3d  bias=%+7.4f  (vs struct 1.5: %+7.4f)  SD=%6.4f  RMSE=%6.4f  MCse(bias)=%6.4f  pass=%s\n",
                m, truth_m, s$B_ok, s$bias, s$bias_vs_structural,
                s$sd, s$rmse, s$mc_se_bias, s$pass))
    all_rows[[length(all_rows) + 1L]] <<- row
  }
}

# ---- DGP 1: Baseline (homoskedastic normal) ----
